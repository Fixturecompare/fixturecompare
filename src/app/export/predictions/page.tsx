'use client'

import React, { useEffect, useMemo, useState } from 'react'
import { useSearchParams } from 'next/navigation'
import PredictiveFixtureCard, { FixtureData, PredictionType } from '@/components/PredictiveFixtureCard'
import { getFixtures, getTeams } from '@/lib/footballApi'

// This export page renders a fixed 1200x630 frame that mirrors the live /predictions-live UI
// so a headful renderer (Puppeteer) can capture a pixel-identical PNG.
// IMPORTANT: This page is only for image export. It expects query params to pre-load data.
// Query params:
//   league=PL|PD|SA|FL1|BL1
//   teamAId=<id>
//   teamBId=<id>
//   (optional) selections=[fixtureId:prediction,...] not currently parsed; the cards will still render

interface TeamLite {
  id: number
  name: string
  shortName?: string
  logo?: string
}

const ExportPredictionsPage: React.FC = () => {
  const params = useSearchParams()
  const league = (params.get('league') || '').toUpperCase()
  const teamAId = Number(params.get('teamAId') || '') || undefined
  const teamBId = Number(params.get('teamBId') || '') || undefined

  const [teamA, setTeamA] = useState<TeamLite | null>(null)
  const [teamB, setTeamB] = useState<TeamLite | null>(null)
  const [fixturesA, setFixturesA] = useState<FixtureData[]>([])
  const [fixturesB, setFixturesB] = useState<FixtureData[]>([])

  // Load teams list to resolve names/logos from ids
  useEffect(() => {
    let mounted = true
    const run = async () => {
      if (!league) return
      try {
        const res: any = await getTeams(league)
        const teamsArr: TeamLite[] = res && typeof res === 'object' && 'success' in res
          ? (res.success ? (res.data?.teams || []) : [])
          : (res as TeamLite[])
        if (!mounted) return
        const a = teamsArr.find(t => t.id === teamAId) || null
        const b = teamsArr.find(t => t.id === teamBId) || null
        setTeamA(a)
        setTeamB(b)
      } catch (e) {
        // ignore; page should still render frame
      }
    }
    run()
    return () => { mounted = false }
  }, [league, teamAId, teamBId])

  // Load fixtures for both teams
  useEffect(() => {
    let mounted = true
    const run = async () => {
      try {
        if (teamAId) {
          const resA: any = await getFixtures(teamAId, league)
          const arrA: any[] = resA && typeof resA === 'object' && 'success' in resA
            ? (resA.success ? (resA.data?.fixtures || []) : [])
            : (resA as any[])
          if (mounted) setFixturesA(arrA as FixtureData[])
        }
        if (teamBId) {
          const resB: any = await getFixtures(teamBId, league)
          const arrB: any[] = resB && typeof resB === 'object' && 'success' in resB
            ? (resB.success ? (resB.data?.fixtures || []) : [])
            : (resB as any[])
          if (mounted) setFixturesB(arrB as FixtureData[])
        }
      } catch (e) {
        // ignore
      }
    }
    run()
    return () => { mounted = false }
  }, [league, teamAId, teamBId])

  const [predictions, setPredictions] = useState<Record<number, PredictionType>>({})
  const hasPredictions = Object.keys(predictions).length > 0

  // Watermark
  const Watermark = () => (
    <div className="absolute bottom-6 right-6 flex items-center space-x-2 opacity-90">
      <img
        src="/logo/fixture-compare-logo.png"
        alt="Fixture Compare logo"
        title="Fixture Compare"
        width={160}
        height={40}
        crossOrigin="anonymous"
        className="w-[160px] h-auto"
        onError={(e) => { (e.currentTarget as HTMLImageElement).style.display = 'none' }}
      />
      <span className="text-gray-700 text-sm opacity-85">Generated by Fixture Compare</span>
    </div>
  )

  // Fixed 1200x630 capture frame with identical background styling
  return (
    <div
      className="relative"
      style={{ width: 1200, height: 630, background: 'linear-gradient(135deg, #a78bfa 0%, #e9d5ff 55%, #f3e8ff 100%)' }}
    >
      <div className="absolute inset-0 p-8 flex flex-col">
        {/* Header with logo (matches /predictions-live header) */}
        <div className="flex justify-center mb-6">
          <img
            src="/Asset%2010.png"
            alt="Fixture Compare"
            width={275}
            height={75}
            crossOrigin="anonymous"
            className="w-[275px] h-auto"
          />
        </div>

        {/* Teams and Summary row */}
        <div className="bg-white rounded-lg border border-gray-400 shadow p-6 flex-1">
          <div className="grid grid-cols-2 gap-6">
            {/* Team A summary */}
            <div className="text-center">
              <div className="flex items-center justify-center space-x-2 mb-2">
                {teamA?.logo ? (
                  <img src={teamA.logo} alt={`${teamA.name} crest`} className="w-6 h-6 object-contain" crossOrigin="anonymous" />
                ) : (
                  <div className="w-6 h-6 rounded-full bg-primary-100 flex items-center justify-center text-primary-600 font-bold text-xs">
                    {(teamA?.name?.trim()?.charAt(0) || 'U').toUpperCase()}
                  </div>
                )}
                <span className="font-semibold text-gray-900 truncate max-w-[220px]" title={teamA?.name}>{teamA?.name || 'Team A'}</span>
              </div>
              {/* Points placeholder: export focuses on visual parity; points can be injected via query in future */}
              <div className="text-3xl font-bold text-primary-600 font-montserrat">—</div>
              <div className="text-sm text-gray-500">projected points</div>
            </div>

            {/* Team B summary */}
            <div className="text-center">
              <div className="flex items-center justify-center space-x-2 mb-2">
                {teamB?.logo ? (
                  <img src={teamB.logo} alt={`${teamB.name} crest`} className="w-6 h-6 object-contain" crossOrigin="anonymous" />
                ) : (
                  <div className="w-6 h-6 rounded-full bg-primary-100 flex items-center justify-center text-primary-600 font-bold text-xs">
                    {(teamB?.name?.trim()?.charAt(0) || 'U').toUpperCase()}
                  </div>
                )}
                <span className="font-semibold text-gray-900 truncate max-w-[220px]" title={teamB?.name}>{teamB?.name || 'Team B'}</span>
              </div>
              <div className="text-3xl font-bold text-primary-600 font-montserrat">—</div>
              <div className="text-sm text-gray-500">projected points</div>
            </div>
          </div>

          {/* Fixtures grid (optional visual parity) */}
          <div className="grid grid-cols-2 gap-4 mt-6">
            <div className="space-y-3">
              {fixturesA.slice(0, 5).map(fx => (
                <PredictiveFixtureCard
                  key={`A-${fx.id}`}
                  fixture={fx}
                  prediction={predictions[fx.id]}
                  onPredictionChange={(id, p) => setPredictions(s => ({ ...s, [id]: p }))}
                  teamName={teamA?.name}
                  teamLogo={teamA?.logo}
                />
              ))}
            </div>
            <div className="space-y-3">
              {fixturesB.slice(0, 5).map(fx => (
                <PredictiveFixtureCard
                  key={`B-${fx.id}`}
                  fixture={fx}
                  prediction={predictions[fx.id]}
                  onPredictionChange={(id, p) => setPredictions(s => ({ ...s, [id]: p }))}
                  teamName={teamB?.name}
                  teamLogo={teamB?.logo}
                />
              ))}
            </div>
          </div>
        </div>

        {/* Watermark */}
        <Watermark />
      </div>
    </div>
  )
}

export default ExportPredictionsPage
